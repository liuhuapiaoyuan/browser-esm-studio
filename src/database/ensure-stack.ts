function viteConfigWithDdbProxy(existing: string): string {
  if (existing.includes('"/ddb"') || existing.includes("'/ddb'")) return existing;

  return `import { defineConfig } from "vite";
import react from "@vitejs/plugin-react";

const DDB_API_BASE_URL = ${JSON.stringify(DYNAMIC_DB_BASE_URL)};

export default defineConfig({
  plugins: [react()],
  server: {
    proxy: {
      "/ddb": {
        target: DDB_API_BASE_URL,
        changeOrigin: true,
        rewrite: (path) => path.replace(/^\\/ddb/, ""),
      },
    },
  },
});
`;
}

function addFileIfMissing(sandbox: Sandbox, path: string, content: string, copied: string[]) {
  if (sandbox.exists(path)) return;
  try {
    sandbox.add(path, content);
    copied.push(path);
  } catch {
    sandbox.write(path, content);
    copied.push(path);
  }
}

function writeAlways(sandbox: Sandbox, path: string, content: string, written: string[]) {
  if (sandbox.exists(path)) {
    sandbox.write(path, content);
  } else {
    sandbox.add(path, content);
  }
  written.push(path);
}

/** 补齐虚拟项目 Dynamic DB 栈：缺啥补啥；binding/.env 始终对齐当前租户 */
export function ensureDdbStack(
  sandbox: Sandbox,
  options: EnsureDdbStackOptions,
): { packageJsonPatched: boolean; filesCopied: string[]; filesUpdated: string[] } {
  const projectId = options.projectId.trim();
  const userId = options.userId?.trim() || getDynamicDbUserId();
  const roles = resolveDynamicDbUserRoles(options.roles);
  const filesCopied: string[] = [];
  const filesUpdated: string[] = [];

  let packageJsonPatched = false;
  if (sandbox.exists("package.json")) {
    try {
      const pkg = JSON.parse(sandbox.read("package.json")) as {
        scripts?: Record<string, string>;
        dependencies?: Record<string, string>;
        devDependencies?: Record<string, string>;
      };
      let changed = false;
      pkg.scripts ??= {};
      if (!pkg.scripts["ddb:codegen"]) {
        pkg.scripts["ddb:codegen"] = "node scripts/ddb-codegen.mjs";
        changed = true;
      }
      pkg.dependencies ??= {};
      if (!pkg.dependencies["@qzsy/dynamic-db-client"]) {
        pkg.dependencies["@qzsy/dynamic-db-client"] = "^0.1.1";
        changed = true;
      }
      pkg.devDependencies ??= {};
      if (!pkg.devDependencies["@qzsy/dynamic-db-cli"]) {
        pkg.devDependencies["@qzsy/dynamic-db-cli"] = "^0.1.2";
        changed = true;
      }
      if (changed) {
        sandbox.write("package.json", `${JSON.stringify(pkg, null, 2)}\n`);
        packageJsonPatched = true;
      }
    } catch {
      /* ignore invalid package.json */
    }
  }

  const envExisting = sandbox.exists(".env") ? sandbox.read(".env") : "";
  writeAlways(sandbox, ".env", buildEnvContent(envExisting, projectId, userId, roles), filesUpdated);
  writeAlways(sandbox, "src/config/ddb-binding.ts", bindingTs(projectId, userId, roles), filesUpdated);

  addFileIfMissing(sandbox, "src/config/ddb.ts", ddbConfigTs(), filesCopied);
  addFileIfMissing(sandbox, "src/lib/db.ts", dbTs(), filesCopied);
  addFileIfMissing(sandbox, "scripts/ddb-codegen.mjs", codegenScript(), filesCopied);
  addFileIfMissing(sandbox, "docs/DYNAMIC-DB.md", docsMd(), filesCopied);

  if (sandbox.exists("vite.config.ts")) {
    const next = viteConfigWithDdbProxy(sandbox.read("vite.config.ts"));
    if (next !== sandbox.read("vite.config.ts")) {
      sandbox.write("vite.config.ts", next);
      filesUpdated.push("vite.config.ts");
    }
  } else {
    addFileIfMissing(sandbox, "vite.config.ts", viteConfigWithDdbProxy(""), filesCopied);
  }

  if (!sandbox.exists("src/ddb/generated/index.ts")) {
    const generated = emptyGeneratedDdbFiles();
    for (const [path, content] of Object.entries(generated)) {
      addFileIfMissing(sandbox, path, content, filesCopied);
    }
  }

  return { packageJsonPatched, filesCopied, filesUpdated };
}
